// loop in cpp 
# include<iostream>
using namespace std;

int main()
{
    // For loop 
    cout<<"FOR LOOP"<<endl;
    for (int i=0; i<40; i++)
        {
            cout<<i<<endl;
        }
    //while loop
    cout<<"WHILE LOOP"<<endl;
    int j=0;
    while (j<40){
        cout<<j<<endl;
        j++;
    }
    cout<<"do while loop";
    int x=10;
    do
    {
        cout<<x<<endl;
        x++;
    } while (x<=40);
    

    return 0;

}